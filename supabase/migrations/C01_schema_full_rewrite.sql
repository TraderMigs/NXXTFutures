-- ── admin_notifications ───────────────────────────────────────
-- System alerts surfaced in the admin dashboard (e.g. Yahoo Finance errors)
CREATE TABLE IF NOT EXISTS admin_notifications (
  id         uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  type       text NOT NULL,
  title      text NOT NULL,
  message    text,
  severity   text DEFAULT 'INFO' CHECK (severity IN ('LOW','INFO','HIGH','CRITICAL')),
  read       boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE admin_notifications ENABLE ROW LEVEL SECURITY;

CREATE POLICY "admins read notifications" ON admin_notifications
  FOR SELECT USING (
    EXISTS (SELECT 1 FROM profiles p WHERE p.id = auth.uid() AND p.is_admin = true)
  );

CREATE POLICY "service role full access notifications" ON admin_notifications
  USING (auth.role() = 'service_role');
